class MusicModel {
  final String? title;
  final String? artist;
  final String? album;
  final String? albumArt;
  final String localUrl;
  final String remoteUrl;

  MusicModel({
    required this.title,
    required this.artist,
    required this.album,
    required this.albumArt,
    required this.localUrl,
    required this.remoteUrl,
  });


}